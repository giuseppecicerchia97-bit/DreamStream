export const getLanguageOption = (code: AppLanguageCode) => {
  return LANGUAGE_OPTIONS.find((language) => language.code === code) ?? LANGUAGE_OPTIONS[0];
};

export const isAppLanguageCode = (value: unknown): value is AppLanguageCode => {
  return typeof value === 'string' && LANGUAGE_OPTIONS.some((language) => language.code === value);
};
